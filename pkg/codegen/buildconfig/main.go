// This program generates a Go file containing a set of exported constants that represent
// configuration variables of Rancher at build-time.
package main

import (
	"errors"
	"fmt"
	"io"
	"os"
	"syscall"
	"text/template"

	"gopkg.in/yaml.v3"
)

func main() {
	// Read build.yaml once
	cfg, err := loadBuildConfig()
	if err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}

	if err := generateGoConstantsFile(cfg); err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
	if err := updateChartValues(cfg); err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
}

func loadBuildConfig() (map[string]string, error) {
	f, err := os.Open("build.yaml")
	if err != nil {
		return nil, fmt.Errorf("failed to open build.yaml: %w", err)
	}
	defer f.Close()

	b, err := io.ReadAll(f)
	if err != nil {
		return nil, fmt.Errorf("failed to read build.yaml: %w", err)
	}
	if len(b) == 0 {
		return nil, fmt.Errorf("build.yaml is empty")
	}

	var cfg map[string]string
	if err := yaml.Unmarshal(b, &cfg); err != nil {
		return nil, fmt.Errorf("failed to unmarshal build.yaml: %w", err)
	}

	return cfg, nil
}

func generateGoConstantsFile(cfg map[string]string) error {
	out, err := os.OpenFile("pkg/buildconfig/constants.go", os.O_TRUNC|os.O_WRONLY, 0644)
	if err != nil {
		return err
	}
	defer out.Close()
	const raw = `// Code generated by pkg/codegen/buildconfig/main.go. DO NOT EDIT.
// Package buildconfig contains a set of exported constants that represent configuration variables of Rancher at build-time.
package buildconfig

const (
{{ . }})
`
	tmpl, err := template.New("").Parse(raw)
	if err != nil {
		return err
	}
	writer := GoConstantsWriter{
		Tmpl:   tmpl,
		Config: cfg,
		Output: out,
	}
	return writer.Run()
}

func updateChartValues(cfg map[string]string) error {
	// Read chart/values.yaml
	chartValues, err := os.Open("chart/values.yaml")
	if err != nil {
		return err
	}
	defer chartValues.Close()

	// Create temp file for output
	tempFile, err := os.CreateTemp("", "values-*.yaml")
	if err != nil {
		return err
	}
	tempPath := tempFile.Name()
	defer os.Remove(tempPath) // Clean up temp file on error

	// Update chart values
	writer := ChartValuesWriter{
		Config: cfg,
		Chart:  chartValues,
		Output: tempFile,
	}
	if err := writer.Run(); err != nil {
		tempFile.Close()
		return err
	}
	if err := tempFile.Close(); err != nil {
		return err
	}

	// Replace original using the safe cross-device rename helper
	if err := safeRename(tempPath, "chart/values.yaml"); err != nil {
		return err
	}

	return nil
}

// safeRename attempts a fast OS rename. If it detects a cross-device
// link error (EXDEV), it falls back to a copy-and-delete strategy.
func safeRename(src, dst string) error {
	// Only works on the same partition
	err := os.Rename(src, dst)
	if err == nil {
		return nil
	}

	// Check if the error is specifically an "invalid cross-device link"
	if linkErr, ok := errors.AsType[*os.LinkError](err); ok && errors.Is(linkErr.Err, syscall.EXDEV) {
		// Different partitions detected, execute copy-and-delete fallback
		return copyAndDelete(src, dst)
	}

	// Return any other legitimate error (e.g., Permission Denied)
	return err
}

func copyAndDelete(src, dst string) error {
	srcFile, err := os.Open(src)
	if err != nil {
		return err
	}
	defer srcFile.Close()

	dstFile, err := os.Create(dst)
	if err != nil {
		return err
	}
	defer dstFile.Close()

	// Stream the data from /tmp to the destination
	if _, err = io.Copy(dstFile, srcFile); err != nil {
		return err
	}

	// Safely flush memory to disk before deleting the source
	if err = dstFile.Sync(); err != nil {
		return err
	}

	// Close files explicitly before removing the source
	srcFile.Close()
	dstFile.Close()

	// Remove the temporary file from /tmp
	return os.Remove(src)
}
